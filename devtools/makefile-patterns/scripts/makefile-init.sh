}

# ─── Main ───────────────────────────────────────────────────

[[ $# -lt 1 ]] && usage 1

TYPE="$1"
OUTPUT="${2:--}"

case "$TYPE" in
    go)     content=$(generate_go) ;;
    node)   content=$(generate_node) ;;
    python) content=$(generate_python) ;;
    docker) content=$(generate_docker) ;;
    c)      content=$(generate_c) ;;
    cpp|c++) content=$(generate_cpp) ;;
    -h|--help) usage 0 ;;
    *)
        echo "Error: Unknown project type '$TYPE'" >&2
        echo "Valid types: go, node, python, docker, c, cpp" >&2
        exit 1
        ;;
esac

if [[ "$OUTPUT" == "-" ]]; then
    echo "$content"
else
    # Create parent directories if needed
    mkdir -p "$(dirname "$OUTPUT")"
    if [[ -f "$OUTPUT" ]]; then
        echo "Warning: $OUTPUT already exists. Overwrite? [y/N]" >&2
        read -r ans
        [[ "$ans" != "y" && "$ans" != "Y" ]] && exit 1
    fi
    echo "$content" > "$OUTPUT"
    echo "Generated $TYPE Makefile → $OUTPUT" >&2
fi
